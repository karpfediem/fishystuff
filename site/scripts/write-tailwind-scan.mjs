import { mkdir, readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const siteDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const outputDir = path.join(siteDir, ".tailwind");
const outputPath = path.join(outputDir, "source.html");

const ROOTS = [
  { dir: "layouts", exts: new Set([".shtml", ".html"]) },
  { dir: "content", exts: new Set([".smd", ".md"]) },
  { dir: "assets", exts: new Set([".js", ".mjs", ".ts"]) },
  { dir: "scripts", exts: new Set([".js", ".mjs", ".ts"]) },
];

const CAPTURE_PATTERNS = [
  /class\s*=\s*(["'])([\s\S]*?)\1/g,
  /createElement\(\s*[^,]+,\s*(["'`])([\s\S]*?)\1/g,
  /setAttribute\(\s*["']class["']\s*,\s*(["'`])([\s\S]*?)\1\)/g,
  /\.className\s*=\s*(["'`])([\s\S]*?)\1/g,
];

const CLASSLIST_PATTERN = /classList\.(?:add|remove|toggle|contains)\(([\s\S]*?)\)/g;
const STRING_LITERAL_PATTERN = /(["'`])((?:\\.|(?!\1)[\s\S])*)\1/g;

async function walk(dir, exts) {
  const entries = await readdir(dir, { withFileTypes: true });
  const files = [];
  for (const entry of entries) {
    if (entry.name === ".out" || entry.name === ".tailwind" || entry.name === "node_modules") {
      continue;
    }
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...await walk(fullPath, exts));
      continue;
    }
    if (exts.has(path.extname(entry.name))) {
      files.push(fullPath);
    }
  }
  return files;
}

function normalizeClassValue(value) {
  if (value.includes("${")) {
    return "";
  }
  return value.replace(/\s+/g, " ").trim();
}

function escapeAttribute(value) {
  return value
    .replace(/&/g, "&amp;")
    .replace(/"/g, "&quot;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;");
}

function collectClassValues(sourceText) {
  const values = new Set();

  for (const pattern of CAPTURE_PATTERNS) {
    for (const match of sourceText.matchAll(pattern)) {
      const value = normalizeClassValue(match[2]);
      if (value.length > 0) {
        values.add(value);
      }
    }
  }

  for (const match of sourceText.matchAll(CLASSLIST_PATTERN)) {
    const classes = [];
    for (const stringMatch of match[1].matchAll(STRING_LITERAL_PATTERN)) {
      const value = normalizeClassValue(stringMatch[2]);
      if (value.length > 0) {
        classes.push(value);
      }
    }
    if (classes.length > 0) {
      values.add(classes.join(" "));
    }
  }

  return values;
}

const classValues = new Set();

for (const root of ROOTS) {
  const baseDir = path.join(siteDir, root.dir);
  const files = await walk(baseDir, root.exts);
  for (const filePath of files) {
    const sourceText = await readFile(filePath, "utf8");
    for (const classValue of collectClassValues(sourceText)) {
      classValues.add(classValue);
    }
  }
}

const lines = [
  "<!-- Generated by scripts/write-tailwind-scan.mjs -->",
  ...Array.from(classValues).sort().map((classValue) => `<div class="${escapeAttribute(classValue)}"></div>`),
  "",
];

await mkdir(outputDir, { recursive: true });
await writeFile(outputPath, lines.join("\n"), "utf8");
